#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	j;

	j = 0;
	i = argc - 1;
	while (i)
	{
		while (argv[i][j])
		{
			write(1, &argv[i][j], 1);
			j++;
		}
		write(1, "\n", 1);
		i--;
		j = 0;
	}
	return (0);
}
